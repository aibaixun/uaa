package com.aibaixun.gail.entity;

public class NameJson {
    private String id;
    private String name;

    public NameJson() {
    }

    public NameJson(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
